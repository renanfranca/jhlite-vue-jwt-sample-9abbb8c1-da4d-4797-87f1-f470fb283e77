import type { LoginCredentials } from '@/auth/domain/LoginCredentials';

type RestLoginCredentialsUserName = string;
type RestLoginCredentialsPassword = string;

export type RestLoginCredentials = {
  username: RestLoginCredentialsUserName;
  password: RestLoginCredentialsPassword;
};

export const toRestLoginCredentials = (loginCredentials: LoginCredentials): RestLoginCredentials => ({
  username: loginCredentials.username,
  password: loginCredentials.password,
});
